public class IdiomJson {

    /**
     * ex : dhjdj
     * id : 1
     * name : dhhdh
     * baseObjId : 1
     */

    private String ex;
    private int id;
    private String name;
    private int baseObjId;

    public String getEx() {
        return ex;
    }

    public void setEx(String ex) {
        this.ex = ex;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getBaseObjId() {
        return baseObjId;
    }

    public void setBaseObjId(int baseObjId) {
        this.baseObjId = baseObjId;
    }
}
